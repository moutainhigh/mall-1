      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在申请全残保险金时，申请人须填写保险金给付申请书，并提供下列证明和资料：</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1. 保险合同原件或其他保险凭证；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2. 被保险人的有效身份证件； </p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3. 由司法鉴定机构或其他有资质的医疗鉴定机构出具的被保险人全残证明；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4. 受益人所能提供的与确认保险事故的性质、原因等相关的其他证明、资料。</p>
