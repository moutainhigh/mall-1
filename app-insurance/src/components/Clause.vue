        按被保险人身故或全残时本主险合同实际交纳的保险费与本主险合同的现金价值（释义六）二者的 较大者给付身故或全残保险金，本主险合同终止。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;若被保险人于祝寿金领取日零时后身故或全残，本公司将按被保险人身故或全残时本主险合同
        的现金价值给付身故或全残保险金，本主险合同终止。</p>
