      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3. 投保人的户籍注销证明及国务院卫生行政部门规定的医疗机构、公安部门或者其他有权机
        构出具的投保人的死亡证明或由司法鉴定机构、其他有资质的医疗鉴定机构出具的投保人全残证 明；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4. 受益人所能提供的与确认保险事故的性质、原因等相关的其他证明、资料。</p>
