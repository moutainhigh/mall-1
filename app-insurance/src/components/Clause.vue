      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1. 若被保险人于本主险合同第五个保险合同周年日（释义二）仍生存，本公司将按本主险合
        同实际交纳的保险费的20%给付生存保险金；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2. 若被保险人于本主险合同第六个保险合同周年日及之后的每个保险合同周年日仍生存，且
        被保险人未满六十周岁（释义三），本公司将按本主险合同基本保险金额的20%给付生存保险金，
        直至被保险人年满五十九周岁后的首个保险合同周年日止，本主险合同继续有效；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3. 若被保险人于本主险合同第六个保险合同周年日及之后的每个保险合同周年日仍生存，且
        被保险人年满六十周岁，本公司将按本主险合同基本保险金额的40%给付生存保险金，直至本主 险合同终止。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;二、祝寿金给付</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;祝寿金领取年龄分为六十周岁和七十五周岁两种。若投保时被保险人的年龄未满四十一周岁，
        投保人在投保时可选择其中一种作为本主险合同的祝寿金领取年龄。若投保时被保险人的年龄已满 四十一周岁（含四十一周岁），投保人在投保时只能选择七十五周岁作为祝寿金领取年龄。本主险
        合同的祝寿金领取年龄将载明于保险单上。祝寿金领取年龄一经确定，在本主险合同的保险期间内 不得变更。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;若被保险人于本主险合同约定的祝寿金领取日（释义四）仍生存，本公司将按本主险合同实际
        交纳的保险费给付祝寿金。</p>
