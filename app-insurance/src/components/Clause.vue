        百八十日内（含第一百八十日）因该意外伤害事故导致身故或全残，且投保人身故或全残时未满六 十周岁，本公司将豁免自投保人身故或被鉴定为全残之日起应交的续期保险费，本主险合同继续有 效。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;豁免保险费的，本公司视同自投保人身故或被鉴定为全残之日起的续期保险费已经交纳。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在交费期间内变更投保人的，本公司不予豁免保险费。</p>
