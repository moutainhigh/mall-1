      <p>第十八条 年龄确定与错误处理</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一、被保险人的投保年龄以法定证件登记的周岁计算，且须符合本公司当时的投保规定。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;二、投保人在申请投保时，应将与有效身份证件相符的被保险人的出生日期在投保单上填明</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如果发生错误按照下列方式办理：</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1. 投保人申报的被保险人年龄不真实，并且其真实年龄不符合本公司投保规定的，本公司有
        权解除合同，并向投保人退还本主险合同的现金价值。本公司行使合同解除权适用“本公司合同解 除权的限制”的规定。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2. 投保人申报的被保险人年龄不真实，致使投保人实付保险费少于应付保险费的，本公司有
        权更正年龄并要求投保人补交保险费。若已经发生保险事故，在给付保险金时按本主险合同实际 交纳保险费和应付保险费的比例给付。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3. 投保人申报的被保险人年龄不真实，致使投保人实付保险费多于应付保险费的，本公司会
        将多收的保险费无息退还给投保人。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本公司在收到保险金给付申请书时，有权要求申请人出具被保险人的年龄证明文件。</p>
