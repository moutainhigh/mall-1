      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本主险合同的交费方式和交费期间由投保人和本公司约定，但须符合本公司当时的投保规定，
        约定的交费方式和交费期间将载明于保险单上。若投保人选择分期支付保险费，投保人在支付了首 期保险费后，应按本主险合同的约定支付余下各期保险费。</p>
      <p>第八条 宽限期及保险合同效力的中止</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;分期支付保险费的，投保人支付首期保险费后，除本主险合同另有约定外，如果投保人到期未
        支付当期保险费，自保险合同约定的保险费支付日的次日起六十日为宽限期。宽限期内发生的保险 事故，本公司仍会承担保险责任，但在给付保险金时会扣减投保人欠交的保险费。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如果投保人在宽限期届满前仍未支付当期保险费，则本主险合同自宽限期届满的次日零时起
        效力中止。本主险合同效力中止期间发生保险事故的，本公司不承担保险责任。</p>
      <p>第九条 保单贷款</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在犹豫期结束后，若本主险合同具有现金价值，经被保险人书面同意，投保人可以以书面形式
        向本公司申请保单贷款，累积的保单贷款本息金额以投保人提出书面申请时本主险合同所具有的现 金价值的百分之八十为限，同时须符合本公司当时保单贷款规定。当本主险合同所欠交保险费和累
        积保单贷款本息的总金额超过本主险合同的现金价值当日二十四时起，本主险合同效力中止。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;每次保单贷款的最低金额不得少于人民币伍佰元，贷款期限不得超过六个月。贷款利息在贷款
        到期时应与本金一并归还。若到期未能足额偿还贷款本息，则所欠的贷款本息将作为新的贷款本金 计息。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;贷款利息按投保人与本公司约定的利率执行，逾期期间的利率按约定的利率上浮一个百分点执行。</p>
      <p>第十条 减少保险金额</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本主险合同已交足两年以上保险费且生效满两年后，投保人可以申请减少保险金额，但须符合
        本公司当时的减少保险金额规定。如果投保人申请减少保险金额时有保单贷款未还，本公司将不予 办理减少保险金额。减少保险金额后，本主险合同实际交纳的保险费同比例减少，投保人应交纳保 险费按剩余的基本保险金额计算交纳。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本公司按减少后的基本保险金额承担保险责任。</p>
      <p>第十一条 保险合同效力的恢复（简称“复效”）</p>
