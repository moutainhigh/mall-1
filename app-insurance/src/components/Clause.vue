      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;若以上保险金申请的经办人为代理人，则应另提供授权委托书、代理人有效身份证件等证明文件。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;申请人提交的证明和资料不完整的，本公司将及时一次性通知需补充提供的有关证明和资料。</p>
