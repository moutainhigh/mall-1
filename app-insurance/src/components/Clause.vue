      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指保险合同生效日以后每年对应于保险合同生效日的日期。生效日为闰年二月二十九日的，以
        后非闰年对应于生效日的日期为二月二十八日。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;三、周岁</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指以法定身份证件中记载的出生日期为基础计算的年龄，自出生之日起为零周岁，每经过一
        年增加一岁，不足一年的不计。例如，出生日期为2010年10月1日，2010年10月1日至2011 年9月30日期间为0周岁，2011年10月1日至2012年9月30日期间为1周岁，依此类推。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;四、祝寿金领取日</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指被保险人年满本主险合同约定载明于保险单上的祝寿金领取年龄后的首个保险合同周年 日。</p>
