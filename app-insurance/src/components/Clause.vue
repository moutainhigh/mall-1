        确定的，根据已有证明和资料可以确定的数额先予支付；本公司最终确定给付保险金的数额后，将 支付相应的差额。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;六、受益人向本公司请求给付保险金的诉讼时效期间为五年。自其知道或者应当知道保险事故发生之日起计算。</p>
