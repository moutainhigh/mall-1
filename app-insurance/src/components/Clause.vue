      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一、本主险合同实际交纳的保险费</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指投保人依据本主险合同已经向本公司交纳的保险费，如本主险合同发生过减少保险金额情
        形，则实际交纳的保险费为扣除每次减少保险金额所对应的保险费后的余额。</p>
