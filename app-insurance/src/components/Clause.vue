      <p class="section" style="color: #2c3e50">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2.被保险人故意犯罪或者抗拒依法采取的刑事强制措施；</p>
      <p class="section" style="color: #2c3e50">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3.被保险人自本主险合同成立或者合同效力恢复之日起两年内自杀，但被保险人自杀时为无
        民事行为能力人的除外；</p>
      <p class="section" style="color: #2c3e50">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4.被保险人故意自伤；</p>
      <p class="section" style="color: #2c3e50">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;5.被保险人精神和行为障碍（以世界卫生组织颁布的《疾病和有关健康问题的国际统计分类》
        （ICD-10）为准）；</p>
      <p class="section" style="color: #2c3e50">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;6.战争、军事冲突、暴乱或武装叛乱；</p>
      <p class="section" style="color: #2c3e50">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;7.核爆炸、核辐射或核污染。</p>
