      <p class="section" style="color: #2c3e50">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如果投保人、被保险人或受益人故意或者因重大过失未及时通知，致使保险事故的性质、原
        因、损失程度等难以确定的，本公司对无法确定的部分，不承担给付保险金的责任，但本公司通过 其他途径已经及时知道或者应当及时知道保险事故发生或者虽未及时通知但不影响本公司确定保 险事故的性质、原因、损失程度的除外。</p>
