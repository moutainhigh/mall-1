        付保险金的责任，并不退还保险费。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如果投保人因重大过失未履行如实告知义务，对保险事故的发生有严重影响的，对于本合同
        解除前发生的保险事故，本公司不承担给付保险金的责任，但应当无息退还保险费。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本公司在合同订立或复效时已经知道投保人未如实告知的情况的，本公司不得解除合同；发生
        保险事故的，本公司承担给付保险金的责任。</p>
      <p>第十六条 本公司合同解除权的限制</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;前条规定的合同解除权，自本公司知道有解除事由之日起，超过三十日不行使而消灭。自本合
        同成立之日起超过两年的，本公司不得解除合同；发生保险事故的，本公司承担给付保险金的责任。</p>
      <p>第十七条 受益人的指定和变更</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;投保人或者被保险人可以指定一人或多人为身故保险金受益人。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;身故保险金受益人为多人时，可以确定受益顺序和受益份额；如果没有确定份额，各受益人按
        照相等份额享有受益权。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;被保险人为无民事行为能力人或限制民事行为能力人的，可以由其监护人指定受益人。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;投保人或者被保险人可以变更受益人并书面通知本公司。本公司收到变更受益人的书面通知后，
        在保险单或其他保险凭证上批注或附贴批单。 </p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;投保人在指定和变更受益人时，必须经过被保险人书面同意。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;被保险人身故后，有下列情形之一的，保险金作为被保险人的遗产，由本公司依照《中华人民
        共和国继承法》的规定履行给付保险金的义务：</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一、没有指定受益人，或者受益人指定不明无法确定的；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;二、受益人先于被保险人身故，没有其他受益人的；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;三、受益人依法丧失受益权或者放弃受益权，没有其他受益人的。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;受益人与被保险人在同一事件中身故，且不能确定身故先后顺序的，推定受益人身故在先。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;受益人故意造成被保险人死亡、伤残、疾病的，或者故意杀害被保险人未遂的，该受益人丧失
        受益权。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除另有约定外，本合同其他保险金的受益人约定为被保险人本人。</p>
      <p>第十八条 保险事故的通知</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;投保人、被保险人或受益人知道保险事故后应当在十日内通知本公司。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如果投保人、被保险人或受益人故意或者因重大过失未及时通知，致使保险事故的性质、原因、
        损失程度等难以确定的，本公司对无法确定的部分，不承担给付保险金的责任，但本公司通过其他 途径已经及时知道或者应当及时知道保险事故发生或者虽未及时通知但不影响本公司确定保险事 故的性质、原因、损失程度的除外。</p>
      <p>第十九条 司法鉴定</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;若被保险人发生保险事故，本公司有权要求司法鉴定机构对保险事故进行鉴定。</p>
      <p>第二十条 保险金的申请</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一、身故保险金给付的申请</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在申请身故保险金时，申请人须填写保险金给付申请书，并提供下列证明和资料： </p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1.保险合同原件或其他保险凭证；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2.受益人的有效身份证件；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3.国务院卫生行政部门规定的医疗机构、公安部门或者其他有权机构出具的被保险人的死亡证
        明；若被保险人为宣告死亡，须提供人民法院出具的宣告死亡证明文件；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4.被保险人的户籍注销证明；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;5.受益人所能提供的与确认保险事故的性质、原因等相关的其他证明、资料。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;若被保险人在宣告死亡后又重新出现或确知其下落，身故保险金受益人应在知道或应当知道被
        保险人重新出现或确知其下落之日起三十日内，向本公司无息退还已领取的身故保险金。 </p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;保险金作为被保险人遗产时，必须提供可证明合法继承权的相关权利文件。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;二、全残保险金给付的申请</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在申请全残保险金时，申请人须填写保险金给付申请书，并提供下列证明和资料： </p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1.保险合同原件或其他保险凭证；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2.被保险人的有效身份证件；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3.由司法鉴定机构或其他有资质的医疗鉴定机构出具的被保险人全残证明；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4.受益人所能提供的与确认保险事故的性质、原因等相关的其他证明、资料。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;若以上保险金申请的经办人为代理人，则应另提供授权委托书、代理人有效身份证件等证明文
        件。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;申请人提交的证明和资料不完整的，本公司将及时一次性通知需补充提供的有关证明和资料。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;三、本公司在收到保险金给付申请书及合同约定的证明和资料后，将在五个工作日内作出核定；
        情形复杂的，在三十日内作出核定。对属于保险责任的，本公司在与受益人达成给付保险金的协议 后十日内，履行给付保险金义务。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本公司未及时履行前款规定义务的，除支付保险金外，应当赔偿受益人因此受到的损失。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;对不属于保险责任的，本公司自作出核定之日起三日内向受益人发出拒绝给付保险金通知书并
        说明理由。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本公司在收到保险金给付申请书及有关证明和资料之日起六十日内，对给付保险金的数额不能
        确定的，根据已有证明和资料可以确定的数额先予支付；本公司最终确定给付保险金的数额后，将 支付相应的差额。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;四、受益人向本公司请求给付保险金的诉讼时效期间为五年，自其知道或者应当知道保险事故
        发生之日起计算。</p>
