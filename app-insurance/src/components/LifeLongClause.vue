        况决定红利分配方案，如果本公司确定本合同上一会计年度有红利分配，并且本合同在该保险合同 周年日（释义十五）有效，本公司将在前一保险合同周年日保险金额的基础上，增加本合同的保险 金额，并作为下一次年度分红的基础。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本公司每一会计年度将至少向投保人寄送一次红利通知书。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;二、终了分红</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;终了分红所分配的红利包括以下两种：</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1.体恤金</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;被保险人在本合同生效日起一年后身故或全残导致本合同终止时，本公司将根据分红保险业务
        的实际经营状况进行核算，如确定有红利分配，则将该红利以体恤金的形式与身故或全残保险金一 并给付。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2.特别红利</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在本合同生效日起一年后，因投保人解除保险合同、发生责任免除事项、合同效力中止期满未
        达成复效协议等情形导致本合同终止，或因投保人申请年金转换、减少保险金额等情形导致本合同 保险金额减少时，本公司将根据分红保险业务的实际经营状况进行核算，如确定有红利分配，则将
        该红利以特别红利的形式与本合同的现金价值一并给付。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;上述终了红利的数额可能因保险合同特性（包括交费期间、基本保险金额、本合同已经过保险
        年度（释义十六）、性别、投保年龄等）的差异而有所不同。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本合同分红具有不确定性，无法预先设定且各年度并非完全相同。</p>
