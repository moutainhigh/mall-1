      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如果投保人在宽限期届满前仍未支付当期保险费，则本合同自宽限期届满的次日零时起效力
        中止。本合同效力中止期间发生保险事故的，本公司不承担保险责任，且中止红利分配。</p>
