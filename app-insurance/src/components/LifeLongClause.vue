      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本合同已交足两年以上保险费且生效满两年后，投保人可以申请减少保险金额，但须符合本公
        司当时的减少保险金额规定，保险金额中基本保险金额和累积红利保险金额同比例减少，并领取减 少部分对应的现金价值与有可能分配的特别红利。减少保险金额后，保险费按剩余的基本保险金额 计算交纳。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本公司按减少后的保险金额承担保险责任。 </p>
