      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;犹豫期后，若本合同具有现金价值，经被保险人书面同意，投保人可以以书面形式向本公司申
        请保单贷款，累积的保单贷款本息金额以投保人提出书面申请时本合同所具有的现金价值的百分之
        七十为限，同时须符合本公司当时保单贷款规定。当本合同所欠交保险费和累积保单贷款本息的总金额超过本合同的现金价值当日二十四时起，本合同效力中止。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;每次保单贷款的最低金额不得少于人民币伍佰元，贷款期限不得超过六个月。贷款利息在贷款
        到期时应与本金一并归还。若到期未能足额偿还贷款本息，则所欠的贷款本息将作为新的贷款本金 计息。</p>
