      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一、意外伤害事故</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指遭受外来的、突发的、非本意的、非疾病的使身体受到伤害的客观事件。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;二、全残</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本合同中的全残是指下列情形之一：</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1.双目永久完全失明的；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2.两上肢腕关节以上或两下肢踝关节以上缺失的；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3.一上肢腕关节以上及一下肢踝关节以上缺失的；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4.一目永久完全失明及一上肢腕关节以上缺失的；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;5.一目永久完全失明及一下肢踝关节以上缺失的；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;6.四肢关节机能永久完全丧失的；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;7.咀嚼、吞咽机能永久完全丧失的；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8.中枢神经系统机能或胸、腹部脏器机能极度障碍，终身不能从事任何工作，丧失生活自理能
        力的。 </p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;若被保险人同时符合一项以上全残情形时，全残保险金给付以一项为限。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;永久完全：指上述残疾发生之日起经过180日的所有可能恢复机能的治疗，机能仍然完全丧失。
        但眼球摘除等明显无法恢复之情况，不在此限。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;失明：指包括眼球缺失或摘除、或不能辨别明暗、或仅能辨别眼前手动者，最佳矫正视力低于
        国际标准视力表0.02，或视野半径小于5度，并由本公司指定的有资格的眼科医生出具医疗诊断 证明。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;关节机能丧失：指关节永久完全僵硬、或麻痹、或关节不能随意识活动。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;咀嚼、吞咽机能丧失：指由于牙齿以外的原因引起器质障碍或机能障碍，以致不能作咀嚼、吞
        咽运动，除流质食物外不能摄取或吞咽的状态。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;丧失生活自理能力：指食物摄取、大小便始末、穿脱衣服、起居、步行、入浴等，皆不能独立
        进行，需要他人帮助。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;三、本合同实际交纳的保险费</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指投保人依据本合同已经向本公司交纳的保险费。如本合同发生过减少保险金额情形，则实际
        交纳的保险费为扣除每次减少保险金额所对应的保险费后的余额。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;四、周岁</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指以法定身份证件中记载的出生日期为基础计算的年龄，自出生之日起为零周岁，每经过一年
        增加一岁，不足一年的不计。例如，出生日期为2010年10月1日，2010年10月1日至2011年9 月30日期间为0周岁，2011年10月1日至2012年9月30日期间为1周岁，依此类推。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;五、现金价值</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指保险单所具有的价值，通常体现为解除合同时，根据精算原理计算的，由本公司退还的那部
        分金额，包括基本保险金额对应的现金价值和累积红利保险金额对应的现金价值。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;六、猝死</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指外表看似健康的人由于潜在的疾病或者功能障碍所引起的突然的出乎意料的死亡。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;七、殴斗</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指因投保人或被保险人挑衅或故意行为而导致的打斗。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;八、醉酒 </p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指发生事故时当事人每百毫升血液中的酒精含量大于或等于80 毫克。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;九、毒品 </p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指中华人民共和国刑法规定的鸦片、海洛因、甲基苯丙胺（冰毒）、吗啡、大麻、可卡因以及
        国家规定管制的其他能够使人形成瘾癖的麻醉药品和精神药品，但不包括由医生开具并遵医嘱使 用的用于治疗疾病但含有毒品成分的处方药品。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;十、管制药品 </p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指根据《中华人民共和国药品管理法》及有关法规被列为特殊管理的药品，包括麻醉药品、精
        神药品、毒性药品及放射性药品。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;十一、酒后驾驶 </p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指经检测或鉴定，发生事故时车辆驾驶人员每百毫升血液中的酒精含量达到或超过一定的标准，
        公安机关交通管理部门依据《道路交通安全法》的规定认定为饮酒后驾驶或醉酒后驾驶。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;十二、无合法有效驾驶证驾驶</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指下列情形之一：</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1.没有取得驾驶资格； </p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2.驾驶与驾驶证准驾车型不相符合的车辆； </p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3.持审验不合格的驾驶证驾驶；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4.持学习驾驶证学习驾车时，无教练员随车指导，或不按指定时间、路线学习驾车。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;十三、无有效行驶证</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1.未取得行驶证；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2.机动车被依法注销登记的；</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3.未依法按时进行或通过机动车安全技术检验。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;十四、会计年度 </p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;自公历一月一日起至当年十二月三十一日止。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;十五、保险合同周年日</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指保险合同生效日以后每年对应于保险合同生效日的日期。生效日为闰年二月二十九日的，以
        后非闰年对应于生效日的日期为二月二十八日。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;十六、保险年度</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;从保险合同生效日或生效对应日起到次年的保险合同周年日的前一日为一个保险年度。从保
        险合同生效日起到次年的保险合同周年日的前一日为第一个保险年度，以后依次为第二个保险年度、 第三个保险年度等。 </p>
