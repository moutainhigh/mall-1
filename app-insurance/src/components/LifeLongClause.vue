      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本合同效力中止后两年内，投保人可以申请恢复合同效力。经本公司与投保人协商并达成协议，
        在投保人补交保险费及利息（如有）、累积保单贷款本息当日二十四时起，本合同效力恢复。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;自本合同效力中止之日起满两年投保人和本公司未达成协议的，本公司有权解除合同。本公
        司解除合同的，向投保人退还合同效力中止时本合同的现金价值。</p>
