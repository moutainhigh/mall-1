      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;对保险条款中免除本公司责任的条款，本公司在订立合同时应当在投保单、保险单或者其他保
        险凭证上作出足以引起投保人注意的提示，并对该条款的内容以书面或者口头形式向投保人作出明 确说明，未作提示或者明确说明的，该条款不产生效力。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本合同订立或复效时，本公司就投保人和被保险人的有关情况提出询问，投保人应当如实告知。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如果投保人故意或者因重大过失未履行前款规定的如实告知义务，足以影响本公司决定是否
