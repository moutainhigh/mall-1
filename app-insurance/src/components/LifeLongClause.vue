        金。具体领取金额或领取年限等事宜将在转换年金保险合同中约定。</p>
      <p class="section">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;投保人需在拟申请年金转换的保险合同周年日之前至少提前三十天向本公司书面提出转换年
        金申请。</p>
